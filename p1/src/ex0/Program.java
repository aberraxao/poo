package ex0;

/*
Método principal: main()
Para executar um programa de forma independente, torna-se necessário definir um método
principal (vulgarmente designado de main), que é o que irá executar primeiro ao lançar o
programa. Para executar um programa em Java é necessário indicar uma classe que contenha
uma definição do main.

O argumento args é um vetor de String que irá conter os argumentos passados no lançamento
do programa. O vetor será vazio caso não tenham sido passados argumentos.
*/
public class Program {
   public static void main(String[] args) {
      System.out.println("Bye-bye graphical interface");
   }
}